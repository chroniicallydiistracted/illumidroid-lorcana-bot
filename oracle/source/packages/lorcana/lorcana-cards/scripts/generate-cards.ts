#!/usr/bin/env bun
/**
 * Card Generation Script
 *
 * Generates card data from ravensburger-input.json and lorcast-input.json
 * into the src/cards directory (individual TypeScript files) and
 * src/data directory (JSON reference files).
 *
 * Data source strategy:
 *   - Card text: Lorcast (has symbols like {S}, {I}, {D})
 *   - All other data: Ravensburger (stats, images, set info)
 *
 * Card identity and canonicalId:
 *   - One CardDefinition per printing (each set × card number × variant).
 *   - Card id MUST be a 3-character short id (e.g. "wrC"). Do NOT use printing-id format (e.g. set1-098).
 *   - Identity = full name (name + " - " + subtitle). Same full name ⇒ same canonicalId. Each printing gets a unique 3-char id.
 *   - canonicalId: must begin with "ci_" (e.g. ci_xxx).
 *   - Short IDs are 3 chars (0-9, a-z, A-Z); IDs live only on cards; lookups derived from canonical-cards in code.
 *
 * Scope: only EXPANSION sets are included (QUEST and gateway sets are excluded). All rarities within
 * expansion sets are included (common, uncommon, rare, super rare, legendary, plus specialRarity: enchanted, epic, iconic, promo, challenge).
 *
 * Usage:
 *   bun packages/lorcana-cards/scripts/generate-cards.ts
 *
 * Output files:
 *   - src/cards/{set}/{type}/{card}.ts (individual card files)
 *   - src/cards/catalog-data.ts (main aggregator)
 *   - src/cards/index.ts (entry point)
 *   - src/cards/types.ts (type definitions)
 *   - src/data/sets.json
 *   - src/data/canonical-cards.json
 *   - src/data/cards.aux.printing-metadata.json (minimal runtime metadata, replaces printings.json)
 *   - src/data/cards.aux.kv.json (generated by generate-card-aux.ts)
 *   - src/data/cards.aux.validation-report.json (generated by generate-card-aux.ts)
 *
 * Note: printings.json is no longer generated. Use cards.aux.printing-metadata.json + cards.aux.kv.json instead.
 */

